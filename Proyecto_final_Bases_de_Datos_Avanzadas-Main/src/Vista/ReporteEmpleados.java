    private void jButtonGenerarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonGenerarActionPerformed

        listaReporteEmpleados = objcrud.obtenerRepEmpleados();
        Object datos[] = new Object[10];

        modelo = (new DefaultTableModel());
        modelo.addColumn("Nombre_sucursal");
        modelo.addColumn("Nombre_cargo");
        modelo.addColumn("Documento");
        modelo.addColumn("Nombre_empleado");
        modelo.addColumn("Salario");
        modelo.addColumn("Codigo_empleado");
        modelo.addColumn("Fecha_nacimiento");
        modelo.addColumn("Fecha_ingreso");
        modelo.addColumn("Id_sucursal");
        modelo.addColumn("Id_cargo");

        for (int i = 0; i < listaReporteEmpleados.size(); i++) {
            datos[0] = listaReporteEmpleados.get(i).getNombre_sucursal();
            datos[1] = listaReporteEmpleados.get(i).getNombre_cargo();
            datos[2] = listaReporteEmpleados.get(i).getDocumento();
            datos[3] = listaReporteEmpleados.get(i).getNombre_empleado();
            datos[4] = listaReporteEmpleados.get(i).getSalario();
            datos[5] = listaReporteEmpleados.get(i).getCodigo();
            datos[6] = listaReporteEmpleados.get(i).getFecha_nacimiento();
            datos[7] = listaReporteEmpleados.get(i).getFecha_ingreso();
            datos[8] = listaReporteEmpleados.get(i).getId_sucursal();
            datos[9] = listaReporteEmpleados.get(i).getId_cargo();
            modelo.addRow(datos);
        }
        this.jTablReporteEmpleados.setModel(modelo);
        this.jTablReporteEmpleados.setVisible(true);

        int opc = JOptionPane.showConfirmDialog(rootPane, "Desea guardar el reporte en un archivo csv?");

        if (opc == JOptionPane.YES_OPTION) {
            try {
                BufferedWriter outStream = new BufferedWriter(new FileWriter("reporte_empleados.csv", true));

                // Escribir datos
                for (int i = 0; i < listaReporteEmpleados.size(); i++) {
                    datos[0] = listaReporteEmpleados.get(i).getNombre_sucursal();
                    datos[1] = listaReporteEmpleados.get(i).getNombre_cargo();
                    datos[2] = listaReporteEmpleados.get(i).getDocumento();
                    datos[3] = listaReporteEmpleados.get(i).getNombre_empleado();
                    datos[4] = listaReporteEmpleados.get(i).getSalario();
                    datos[5] = listaReporteEmpleados.get(i).getCodigo();
                    datos[6] = listaReporteEmpleados.get(i).getFecha_nacimiento();
                    datos[7] = listaReporteEmpleados.get(i).getFecha_ingreso();
                    datos[8] = listaReporteEmpleados.get(i).getId_sucursal();
                    datos[9] = listaReporteEmpleados.get(i).getId_cargo();
                    outStream.write(datos[0] + " , " + datos[1] + " , " + datos[2] + " , " + datos[3] + " , " + datos[4] + datos[5] + datos[6] + datos[7] + datos[8] + datos[9] + "\n");

                }
                outStream.close();
                JOptionPane.showMessageDialog(null, "Datos guardados en reporte_empleados.csv");
            } catch (IOException e) {
                e.printStackTrace();
                JOptionPane.showMessageDialog(null, "Error al guardar en el archivo CSV");
            }
        }
    }//GEN-LAST:event_jButtonGenerarActionPerformed

